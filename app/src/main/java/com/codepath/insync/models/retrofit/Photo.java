package com.codepath.insync.models.retrofit;


public class Photo {
    String url;
    String thumbnailUrl;
    String id;

    public Photo() {

    }

    public String getUrl() {
        return url;
    }

    public String getThumbnailUrl() {
        return thumbnailUrl;
    }

    public String getId() {
        return id;
    }
}
